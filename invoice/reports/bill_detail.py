def transform_numeric_month_to_text(nbr: int):
    return MONTHS.get(nbr, "Unknown month")


def determine_price_element(element: dict):
    price_approved = element["price_approved"] if element["price_approved"] != STR_NONE else None
    quantity_approved = element["quantity_approved"] if element["quantity_approved"] != STR_NONE else None
    price = float(price_approved) if price_approved else float(element["price"])
    quantity = float(quantity_approved) if quantity_approved else float(element["quantity"])
    final_price = price * quantity
    return final_price


def prepare_claim_content(claim_content: dict):
    description = ""
    for element in claim_content["claim_details"]:
        price = determine_price_element(element)
        name = element["name"]
        element_description = f"{name} - {price} / "
        description += element_description
    description = description[:-3]
    return description


